letters = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l',
           'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']

print(letters[0:3])  # a to c
print(letters[1:4])  # b to d
print(letters[:4])  # First four letters
print(letters[10:])  # Letters from the tenth onward
print(letters[-3:])  # Last three letters
print(letters[-4:-1])   # Letters from the fourth to the second last
print(letters[::2])  # All odd letters
print(letters[1::2])  # All even letters
